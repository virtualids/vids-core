
// zab

var zabModal = document.getElementById("zabModal");
var zab = document.getElementById("zab");
var zabModalClose = document.getElementsByClassName("zabModalClose")[0];

zab.onclick = function() {
    zabModal.style.display = "block";
}

zabModalClose.onclick = function() {
    zabModal.style.display = "none";
}

